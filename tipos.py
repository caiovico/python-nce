# Tipos primitivos em Python

# 1. Inteiros
inteiro = 10
print("Inteiro:", inteiro)

# 2. Ponto flutuante
ponto_flutuante = 10.5
print("Ponto Flutuante:", ponto_flutuante)

# 3. String
string = "Olá, Mundo!"
print("String:", string)

# 4. Booleano
booleano = True
print("Booleano:", booleano)

# 5. Lista
lista = [1, 2, 3, 4, 5]
print("Lista:", lista)

# 6. Tupla
tupla = (1, 2, 3)
print("Tupla:", tupla)

# 7. Dicionário
dicionario = {"chave": "valor"}
print("Dicionário:", dicionario)

# 8. Conjunto
conjunto = {1, 2, 3}
print("Conjunto:", conjunto)